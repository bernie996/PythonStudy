"Assignment Operators"

"The operator = assign a value to a variable"
a1=0
a2=5
a1 = a2
print("0 = 5 : ",a1)

"Compound Operators also exist"

"+= is used to assign a new value to a variable adding another value"
b1 = 10
b2 = 20
b1 += b2 
"Is the same as to say: b1 = b1+b2"
print("10 += 20 : ",b1)

"The same way that += exist, there is a -=, a *=, /=, %=, **= and //="
c1 = 30
c2 = 20
c1 -= c2
print("30 -= 20 : ",c1)

d1 = 5
d2 = 4
d1 *= d2
print("5 *= 4 : ",d1)

e1 = 20
e2 = 2
e1 /= e2
print("20 /= 2 : ",e1)

f1 = 25
f2 = 3
f1 %= f2
print("25 %= 3 : ",f1)

g1 = 2
g2 = 9
g1 **= g2
print("2 **= 9 : ",g1)

h1 = 22
h2 = 10
h1 //= h2
print("22 //= 10 : ",h1)