"Comparison Operators"

"Can be used to see if:"

" == : An operand is equal to another"

a1, a2, a3 = 15 , 25 , 15
r1 = a1 == a2
r2 = a1 == a3 
r3 = a2 == a3
print("15 == 25 :",r1, "\n" ,"15 == 15 :", r2, "\n" ,"25 == 15 :", r3, "\n")

" != : An operand is not equal to another"

b1, b2, b3 = 17 , 27 , 27
s1 = b1 != b2
s2 = b1 != b3 
s3 = b2 != b3
print("17 != 27 :",s1, "\n" ,"17 != 27 :", s2, "\n" ,"27 != 27 :", s3, "\n")

" < and <=  or > and >=  : Are used when a comparission between two operands"
c1,c2,c3,c4,c5 = 31,20,31,45,11
t1 = c1 < c2
t2 = c1 <= c3
t3 = c1 > c4
t4 = c1 >= c5
print("31 < 20 :",t1, "\n" ,"31 <= 31 :", t2, "\n" ,"31 > 45 :", t3, "\n", "31 >= 11 : ",t4,"\n")


